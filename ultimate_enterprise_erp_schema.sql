-- SYSTEM MANAGEMENT
CREATE TABLE IF NOT EXISTS public.system_settings (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    key TEXT UNIQUE NOT NULL,
    value JSONB,
    description TEXT
);

CREATE TABLE IF NOT EXISTS public.module_settings (
