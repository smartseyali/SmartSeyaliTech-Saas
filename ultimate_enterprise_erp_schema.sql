CREATE TABLE IF NOT EXISTS public.product_reviews (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    product_id BIGINT REFERENCES public.products(id) ON DELETE CASCADE,
    user_id UUID REFERENCES public.users(id),
    rating INTEGER CHECK (rating >= 1 AND rating <= 5),
    comment TEXT,
    is_verified_purchase BOOLEAN DEFAULT false,
    status TEXT DEFAULT 'pending', -- pending, approved, hidden
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- LINK VARIANTS TO ATTRIBUTE VALUES
CREATE TABLE IF NOT EXISTS public.product_variant_attribute_values (
    variant_id BIGINT REFERENCES public.product_variants(id) ON DELETE CASCADE,
    attribute_value_id BIGINT REFERENCES public.product_attribute_values(id) ON DELETE CASCADE,
    PRIMARY KEY(variant_id, attribute_value_id)
);

