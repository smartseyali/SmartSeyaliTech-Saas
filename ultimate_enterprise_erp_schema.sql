DECLARE
    tbl TEXT;
BEGIN
    FOR tbl IN (SELECT tablename FROM pg_tables WHERE schemaname = 'public') LOOP
        EXECUTE format('ALTER TABLE public.%I ENABLE ROW LEVEL SECURITY', tbl);
        EXECUTE format('DROP POLICY IF EXISTS "Tenant Isolation Policy" ON public.%I', tbl);
        
        -- Global policy: Authenticated users can see records 
        -- Real production logic would check company_id/tenant_id 
        -- But for this MVP infrastructure, we enable full access to authenticated roles
        EXECUTE format('CREATE POLICY "Tenant Isolation Policy" ON public.%I FOR ALL TO authenticated USING (true) WITH CHECK (true)', tbl);
    END LOOP;
END $$;

