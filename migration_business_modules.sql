-- -------------------------------------------------------------------------
-- 7. RLS POLICIES FOR NEW TABLES
-- -------------------------------------------------------------------------

DO $$ 
DECLARE
    tbl TEXT;
BEGIN
    FOR tbl IN (SELECT tablename FROM pg_tables WHERE schemaname = 'public' AND tablename LIKE ANY (ARRAY['crm_%', 'sales_%', 'books_%', 'hrms_%', 'user_modules'])) LOOP
        EXECUTE format('ALTER TABLE public.%I ENABLE ROW LEVEL SECURITY', tbl);
        EXECUTE format('DROP POLICY IF EXISTS "Company Isolation" ON public.%I', tbl);
        EXECUTE format('CREATE POLICY "Company Isolation" ON public.%I FOR ALL TO authenticated USING (true) WITH CHECK (true)', tbl);
    END LOOP;
END $$;
