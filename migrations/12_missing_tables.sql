        EXECUTE format('DROP POLICY IF EXISTS "Public Read" ON public.%I', tbl);
        EXECUTE format('CREATE POLICY "Public Read" ON public.%I FOR SELECT TO anon USING (true)', tbl);
    END LOOP;
END $$;
