-- 3. ORDERS & FINANCIALS (Strict Audit)
CREATE TABLE IF NOT EXISTS public.ecom_orders (
    id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    company_id BIGINT REFERENCES public.companies(id) ON DELETE CASCADE,
