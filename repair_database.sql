-- This script fixing missing tables and columns for the Smartseyali Admin System.

-- 1. Ensure system_plans table exists
CREATE TABLE IF NOT EXISTS public.system_plans (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name TEXT NOT NULL,
    slug TEXT UNIQUE NOT NULL,
    price_monthly DECIMAL(12,2) DEFAULT 0,
    price_yearly DECIMAL(12,2) DEFAULT 0,
    features JSONB DEFAULT '[]',
    is_active BOOLEAN DEFAULT true,
    sort_order INTEGER DEFAULT 0,
    created_at TIMESTAMPTZ DEFAULT NOW()
);

-- 2. Ensure system_templates has the gallery_images column
DO $$ 
BEGIN 
    IF NOT EXISTS (SELECT 1 FROM information_schema.columns WHERE table_name='system_templates' AND column_name='gallery_images') THEN
        ALTER TABLE public.system_templates ADD COLUMN gallery_images TEXT[] DEFAULT '{}';
    END IF;
END $$;

-- 3. Seed default plans if table is empty
INSERT INTO public.system_plans (name, slug, price_monthly, features, sort_order) 
SELECT 'Standard', 'standard', 29.00, '["Up to 5 Projects", "Basic Analytics", "Standard Templates", "Email Support"]', 1
WHERE NOT EXISTS (SELECT 1 FROM public.system_plans WHERE slug = 'standard');

INSERT INTO public.system_plans (name, slug, price_monthly, features, sort_order) 
SELECT 'Professional', 'professional', 99.00, '["Up to 20 Projects", "Advanced Analytics", "Premium Templates", "Priority Support", "Custom Domain"]', 2
WHERE NOT EXISTS (SELECT 1 FROM public.system_plans WHERE slug = 'professional');

-- 4. Enable RLS and add policies
ALTER TABLE public.system_plans ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.system_templates ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "Global Full Access" ON public.system_plans;
CREATE POLICY "Global Full Access" ON public.system_plans FOR ALL TO authenticated USING (true) WITH CHECK (true);

DROP POLICY IF EXISTS "Global Public Read" ON public.system_plans;
CREATE POLICY "Global Public Read" ON public.system_plans FOR SELECT TO anon USING (true);

